package classroom;

public class playwithwhileloops {
    public static void main(String[] args) {

        int i = 1;
        while(i <= 10) {
            System.out.println(i);
            i++;
        }

        // print even numbers from 2 to 20

        int j = 2;
        while(j <= 20) {
            System.out.println(j);
            j += 2; // j = j + 2
        }
    }
}
